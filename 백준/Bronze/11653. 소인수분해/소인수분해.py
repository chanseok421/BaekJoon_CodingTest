n = int(input())  # 소인수분해할 수 N을 입력받음
i = 2             # 소인수 후보 시작 값 (가장 작은 소수인 2부터 시작)

while n > 1:      # N이 1이 될 때까지 반복 (모든 소인수를 다 나눌 때까지)
    if n % i == 0:        # i가 N의 약수이면 (= 나눠떨어지면)
        print(i)          # i는 소인수이므로 출력
        n //= i           # N을 i로 나눈 몫으로 갱신 (i를 N에서 제거)
    else:
        i += 1            # i가 약수가 아니면 다음 수로 증가 (다음 후보 검사)
